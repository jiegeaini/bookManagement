package com.book.pojo;

/**
 * @author jiege
 * @explain 书籍分类实体类
 * @time 2019/3/15 0:43
 */
public class Category {
	/**分类id*/
	private int id;
	/**分类名称*/
	private String name;
	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
}
